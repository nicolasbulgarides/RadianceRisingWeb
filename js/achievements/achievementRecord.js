class AchievementRecord {
  constructor(
    playerAbsoluteId,
    playerName,
    playerLevelAtTimeOfAchievement,
    achievementTimestamp
  ) {
    this.playerAbsoluteId = playerAbsoluteId;
    this.playerName = playerName;
    this.playerLevelAtTimeOfAchievement = playerLevelAtTimeOfAchievement;
    this.achievementTimestamp = achievementTimestamp;
  }
}
